//==== або  switch-case==============

let seasonNumber = prompt("Введіть число від 1 до 4");
let seasonName;

switch (seasonNumber) {
  case '1':
    seasonName = 'зима';
    break;
  case '2':
    seasonName = 'весна';
    break;
  case '3':
    seasonName = 'літо';
    break;
  case '4':
    seasonName = 'осінь';
    break;
  default:
    seasonName = "Вибачте, але ви маєте ввести значення від 1 до 4 включно";
}

console.log(seasonName);


